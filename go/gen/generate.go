package gen

import (
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"unicode"

	"github.com/marcisbee/flop"
)

type Options struct {
	OutDir    string
	GoOutDir  string
	GoPackage string
}

func GenerateFromSpecFile(specPath string, opts Options) error {
	data, err := os.ReadFile(specPath)
	if err != nil {
		return err
	}
	var spec flop.AppSpec
	if err := json.Unmarshal(data, &spec); err != nil {
		return err
	}
	return Generate(spec, opts)
}

func Generate(spec flop.AppSpec, opts Options) error {
	outDir := opts.OutDir
	if outDir == "" {
		outDir = "."
	}
	if err := os.MkdirAll(outDir, 0o755); err != nil {
		return err
	}

	files := map[string]string{
		"flop.gen.ts":               renderFlopGen(spec),
		"flop.valibot.gen.ts":       renderValibot(spec),
		"routes.gen.ts":             renderRoutes(spec),
		"flop.react-router.gen.tsx": renderReactRouter(),
	}

	for name, content := range files {
		path := filepath.Join(outDir, name)
		if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
			return err
		}
	}

	if opts.GoOutDir != "" {
		goPkg := opts.GoPackage
		if strings.TrimSpace(goPkg) == "" {
			goPkg = "flopgen"
		}
		if err := os.MkdirAll(opts.GoOutDir, 0o755); err != nil {
			return err
		}
		goFiles := map[string]string{
			"flop.models.gen.go": renderGoModels(spec, goPkg),
			"flop.tables.gen.go": renderGoTables(spec, goPkg),
		}
		for name, content := range goFiles {
			path := filepath.Join(opts.GoOutDir, name)
			if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
				return err
			}
		}
	}

	return nil
}

func renderFlopGen(spec flop.AppSpec) string {
	var b strings.Builder
	b.WriteString("// Code generated by flop-gen. DO NOT EDIT.\n\n")
	b.WriteString("export type FlopTables = {\n")
	for _, t := range sortedTables(spec.Tables) {
		fmt.Fprintf(&b, "  %s: %s;\n", t.Name, t.RowTS)
	}
	b.WriteString("};\n\n")

	b.WriteString("export interface FlopViews {\n")
	for _, v := range sortedEndpoints(spec.Views) {
		fmt.Fprintf(&b, "  %s: { input: %s; output: %s };\n", v.Name, v.InputTS, v.OutputTS)
	}
	b.WriteString("}\n\n")

	b.WriteString("export interface FlopReducers {\n")
	for _, r := range sortedEndpoints(spec.Reducers) {
		fmt.Fprintf(&b, "  %s: { input: %s; output: %s };\n", r.Name, r.InputTS, r.OutputTS)
	}
	b.WriteString("}\n\n")

	b.WriteString("export type ViewName = keyof FlopViews;\n")
	b.WriteString("export type ReducerName = keyof FlopReducers;\n\n")

	specJSON, _ := json.MarshalIndent(spec, "", "  ")
	b.WriteString("export const FLOP_SPEC = ")
	b.Write(specJSON)
	b.WriteString(" as const;\n")

	return b.String()
}

func renderValibot(spec flop.AppSpec) string {
	var b strings.Builder
	b.WriteString("// Code generated by flop-gen. DO NOT EDIT.\n")
	b.WriteString("import * as v from \"valibot\";\n\n")
	b.WriteString("export const viewValidators = {\n")
	for _, ep := range sortedEndpoints(spec.Views) {
		fmt.Fprintf(&b, "  %s: v.any(),\n", quoteIfNeeded(ep.Name))
	}
	b.WriteString("} as const;\n\n")

	b.WriteString("export const reducerValidators = {\n")
	for _, ep := range sortedEndpoints(spec.Reducers) {
		fmt.Fprintf(&b, "  %s: v.any(),\n", quoteIfNeeded(ep.Name))
	}
	b.WriteString("} as const;\n")

	return b.String()
}

func renderRoutes(spec flop.AppSpec) string {
	var b strings.Builder
	b.WriteString("// Code generated by flop-gen. DO NOT EDIT.\n\n")

	type routeDef struct {
		Path  string `json:"path"`
		Entry string `json:"entry"`
	}

	layouts := make([]routeDef, 0, len(spec.Layouts))
	for _, l := range spec.Layouts {
		layouts = append(layouts, routeDef{Path: l.Path, Entry: l.Entry})
	}
	pages := make([]routeDef, 0, len(spec.Pages))
	for _, p := range spec.Pages {
		pages = append(pages, routeDef{Path: p.Path, Entry: p.Entry})
	}

	ljson, _ := json.MarshalIndent(layouts, "", "  ")
	pjson, _ := json.MarshalIndent(pages, "", "  ")

	b.WriteString("export const flopLayouts = ")
	b.Write(ljson)
	b.WriteString(" as const;\n\n")

	b.WriteString("export const flopPages = ")
	b.Write(pjson)
	b.WriteString(" as const;\n")

	return b.String()
}

func renderReactRouter() string {
	return `// Code generated by flop-gen. DO NOT EDIT.
import { createBrowserRouter } from "react-router-dom";
import { flopPages } from "./routes.gen";

export const flopRouter = createBrowserRouter(
  flopPages.map((p) => ({
    path: p.path,
    lazy: async () => {
      const mod = await import(/* @vite-ignore */ p.entry);
      return { Component: mod.default ?? mod.Component ?? (() => null) };
    },
  }))
);
`
}

func renderGoModels(spec flop.AppSpec, pkg string) string {
	tables := sortedTables(spec.Tables)
	typeNames := make(map[string]string, len(tables))
	used := map[string]int{}
	for _, t := range tables {
		typeNames[t.Name] = uniqueTypeName(sanitizeTypeName(t.RowType, t.Name), used)
	}

	var b strings.Builder
	b.WriteString("// Code generated by flop-gen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	if needsFlopImport(spec) {
		b.WriteString("import flop \"github.com/marcisbee/flop\"\n\n")
	}
	for _, t := range tables {
		typeName := typeNames[t.Name]
		fmt.Fprintf(&b, "type %s struct {\n", typeName)
		for _, f := range sortedFields(t.Fields) {
			goField := sanitizeFieldName(f.GoName, f.JSONName)
			goType := goTypeForField(f)
			tag := fmt.Sprintf("`json:\"%s", f.JSONName)
			if !f.Required {
				tag += ",omitempty"
			}
			tag += "\"`"
			fmt.Fprintf(&b, "\t%s %s %s\n", goField, goType, tag)
		}
		b.WriteString("}\n\n")
	}
	return mustFormatGoCode(b.String())
}

func renderGoTables(spec flop.AppSpec, pkg string) string {
	tables := sortedTables(spec.Tables)
	if len(tables) == 0 {
		return "// Code generated by flop-gen. DO NOT EDIT.\n\npackage " + pkg + "\n"
	}
	constNames := make(map[string]string, len(tables))
	fieldNames := make(map[string]string, len(tables))
	usedConsts := map[string]int{}
	usedFields := map[string]int{}
	for _, t := range tables {
		base := toGoPublic(t.Name)
		constNames[t.Name] = uniqueTypeName("Table"+base, usedConsts)
		fieldNames[t.Name] = uniqueTypeName(base, usedFields)
	}

	var b strings.Builder
	b.WriteString("// Code generated by flop-gen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	b.WriteString("const (\n")
	for _, t := range tables {
		fmt.Fprintf(&b, "\t%s = %q\n", constNames[t.Name], t.Name)
	}
	b.WriteString(")\n\n")

	b.WriteString("type TableNames struct {\n")
	for _, t := range tables {
		fmt.Fprintf(&b, "\t%s string\n", fieldNames[t.Name])
	}
	b.WriteString("}\n\n")
	b.WriteString("var Tables = TableNames{\n")
	for _, t := range tables {
		fmt.Fprintf(&b, "\t%s: %s,\n", fieldNames[t.Name], constNames[t.Name])
	}
	b.WriteString("}\n")
	return mustFormatGoCode(b.String())
}

func sortedTables(in []flop.TableSpec) []flop.TableSpec {
	out := append([]flop.TableSpec(nil), in...)
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out
}

func sortedEndpoints(in []flop.EndpointSpec) []flop.EndpointSpec {
	out := append([]flop.EndpointSpec(nil), in...)
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out
}

func quoteIfNeeded(s string) string {
	if s == "" {
		return `""`
	}
	r := []rune(s)
	if !(unicode.IsLetter(r[0]) || r[0] == '_' || r[0] == '$') {
		return fmt.Sprintf("%q", s)
	}
	for _, ch := range r[1:] {
		if !(unicode.IsLetter(ch) || unicode.IsDigit(ch) || ch == '_' || ch == '$') {
			return fmt.Sprintf("%q", s)
		}
	}
	return s
}

func sortedFields(in []flop.FieldSpec) []flop.FieldSpec {
	out := append([]flop.FieldSpec(nil), in...)
	sort.Slice(out, func(i, j int) bool { return out[i].JSONName < out[j].JSONName })
	return out
}

func sanitizeTypeName(rowType, tableName string) string {
	n := strings.TrimSpace(rowType)
	if n == "" {
		n = toGoPublic(tableName) + "Row"
	}
	if i := strings.LastIndex(n, "/"); i >= 0 && i+1 < len(n) {
		n = n[i+1:]
	}
	if i := strings.LastIndex(n, "."); i >= 0 && i+1 < len(n) {
		n = n[i+1:]
	}
	n = toGoPublic(n)
	if n == "" {
		n = toGoPublic(tableName) + "Row"
	}
	if !unicode.IsLetter([]rune(n)[0]) {
		n = "Row" + n
	}
	return n
}

func uniqueTypeName(base string, used map[string]int) string {
	if used[base] == 0 {
		used[base] = 1
		return base
	}
	used[base]++
	return fmt.Sprintf("%s%d", base, used[base])
}

func toGoPublic(s string) string {
	parts := splitWords(s)
	if len(parts) == 0 {
		return "Value"
	}
	for i, p := range parts {
		u := strings.ToUpper(p)
		switch u {
		case "ID", "URL", "URI", "UUID", "API", "HTTP", "HTTPS", "IP", "JSON", "JWT", "SQL", "TS":
			parts[i] = u
		default:
			parts[i] = strings.ToUpper(p[:1]) + strings.ToLower(p[1:])
		}
	}
	return strings.Join(parts, "")
}

func splitWords(s string) []string {
	if s == "" {
		return nil
	}
	runes := []rune(s)
	out := make([]string, 0, 4)
	var current []rune
	flush := func() {
		if len(current) == 0 {
			return
		}
		out = append(out, string(current))
		current = current[:0]
	}
	for i, ch := range runes {
		if ch == '_' || ch == '-' || ch == ' ' || ch == '.' || ch == '/' {
			flush()
			continue
		}
		if i > 0 && unicode.IsUpper(ch) && len(current) > 0 && unicode.IsLower(current[len(current)-1]) {
			flush()
		}
		if unicode.IsLetter(ch) || unicode.IsDigit(ch) {
			current = append(current, ch)
		}
	}
	flush()
	return out
}

func sanitizeFieldName(goName, jsonName string) string {
	n := strings.TrimSpace(goName)
	if n == "" {
		n = toGoPublic(jsonName)
	}
	n = toGoPublic(n)
	if n == "" {
		n = "Field"
	}
	if !unicode.IsLetter([]rune(n)[0]) {
		n = "F" + n
	}
	return n
}

func needsFlopImport(spec flop.AppSpec) bool {
	for _, t := range spec.Tables {
		for _, f := range t.Fields {
			if f.Kind == "fileSingle" || f.Kind == "fileMulti" {
				return true
			}
		}
	}
	return false
}

func goTypeForField(f flop.FieldSpec) string {
	var t string
	switch f.Kind {
	case "string", "bcrypt", "refSingle", "ref", "enum":
		t = "string"
	case "number":
		t = "float64"
	case "integer", "timestamp":
		t = "int64"
	case "boolean":
		t = "bool"
	case "roles", "refMulti", "set":
		t = "[]string"
	case "vector":
		t = "[]float64"
	case "json":
		t = "map[string]any"
	case "fileSingle":
		t = "*flop.FileRef"
	case "fileMulti":
		t = "[]flop.FileRef"
	default:
		t = "any"
	}

	if f.Required {
		return t
	}
	if strings.HasPrefix(t, "[]") || strings.HasPrefix(t, "map[") || strings.HasPrefix(t, "*") || t == "any" {
		return t
	}
	return "*" + t
}

func mustFormatGoCode(src string) string {
	out, err := format.Source([]byte(src))
	if err != nil {
		return src
	}
	return string(out)
}
